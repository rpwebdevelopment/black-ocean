    public static function getSystem(int $lat, int $long): array
    {
        $system = self::$planets[$lat][$long];
        $system['lat'] = $lat;
        $system['long'] = $long;
        return $system;
    }

    public static function getAvailableLocations(int $currentLat, int $currentLong, int $parsecs = 1): array
    {
        $minLat = $currentLat - $parsecs;
        $maxLat = $currentLat + $parsecs;
        $even = $currentLat % 2 === 0;
        if ($even) {
            $minLong = $currentLong - ($parsecs - 1);
            $maxLong = $currentLong + $parsecs;
        } else {
            $minLong = $currentLong - $parsecs;
            $maxLong = $currentLong + ($parsecs - 1);
        }

        $available = [];
        for ($lat = $minLat; $lat <= $maxLat; $lat++) {
            for ($long = $minLong; $long <= $maxLong; $long++) {
                if (
                    ($lat === $currentLat && $long === $currentLong)
                    || (!$even && $parsecs > 1 && $long === $minLong && ($lat === $minLat || $lat === $maxLat))
                    || ($even && $parsecs > 1 && $long === $maxLong && ($lat === $minLat || $lat === $maxLat))
                ) {
                    continue;
                }

                $range = 2;
                if (
                    (
                        ($lat === $currentLat - 1 || $lat === $currentLat + 1)
                        && (
                            ($even && ($long === $currentLong || $long === $currentLong + 1))
                            || (!$even && ($long === $currentLong || $long === $currentLong - 1))
                        )
                    )
                    || (
                        $lat === $currentLat
                        && ($long === $currentLong - 1 || $long === $currentLong + 1)
                    )
                ) {
                    $range = 1;
                }

                if (isset(self::$planets[$lat][$long])) {
                    $available[$lat . $long] = array_merge(
                        self::$planets[$lat][$long],
                        [
                            'lat' => $lat,
                            'long' => $long,
                            'distance' => $range,
                        ]
                    );
                }
            }
        }

        $extraLong = $even ? $currentLong - $parsecs : $currentLong + $parsecs;
        if (isset(self::$planets[$currentLat][$extraLong])) {
            $available[$currentLat . $extraLong] = array_merge(
                self::$planets[$currentLat][$extraLong],
                [
                    'lat' => $currentLat,
                    'long' => $extraLong
                ]
            );
        }

        return $available;
    }
